/** 무기 이름에서 무기 상수 가져오기 (기본값 1.0) */
export function getWeaponMultiplier(weaponName: string): number {
  for (const [key, value] of Object.entries(WEAPON_MULTIPLIER)) {
    if (weaponName.includes(key)) return value;
  }
  return 1.0;
}
